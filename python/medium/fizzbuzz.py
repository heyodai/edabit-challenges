def fizz_buzz(num):
    for i in range(1, num + 1):
        print(i)

# test the output
fizz_buzz(3) # "Fizz"
fizz_buzz(5) # "Buzz"
fizz_buzz(15) # "FizzBuzz"
fizz_buzz(4) # "4"